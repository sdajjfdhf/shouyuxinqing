// 快速回复选项
const quickReplyOptions = {
  work: ['工作任务太多', '和同事有矛盾', '压力太大', '想辞职'],
  relationship: ['和朋友吵架了', '想家了', '感到孤独', '不知道怎么表达'],
  emotion: ['很焦虑', '情绪低落', '压力大', '睡不着'],
  daily: ['今天很累', '遇到了开心的事', '感觉很迷茫', '想找人聊聊'],
};

export function generateResponse(options: ReplyOptions): {
  content: string;
  quickReplies?: string[];
} {
  const { userInput, userMood } = options;
  const inputLower = userInput.toLowerCase();

  // 检查关键词
  for (const [keyword, responses] of Object.entries(keywordResponses)) {
    if (inputLower.includes(keyword)) {
      const content = responses[Math.floor(Math.random() * responses.length)];
      const replyCategory = getReplyCategory(keyword);
      return {
        content,
        quickReplies: replyCategory ? quickReplyOptions[replyCategory] : undefined,
      };
    }
  }

  // 如果有情绪状态，结合情绪回复
  if (userMood) {
    const moodResponse = moodResponses[userMood];
    return {
      content: moodResponse[Math.floor(Math.random() * moodResponse.length)],
      quickReplies: quickReplyOptions.emotion,
    };
  }

  // 默认回复
  return {
    content: defaultResponses[Math.floor(Math.random() * defaultResponses.length)],
    quickReplies: quickReplyOptions.daily,
  };
}

function getReplyCategory(keyword: string): keyof typeof quickReplyOptions | null {
  if (['工作', '压力', '加班', '任务太多'].includes(keyword)) {
    return 'work';
  }
  if (['朋友', '家人', '孤独', '矛盾'].includes(keyword)) {
    return 'relationship';
  }
  if (['焦虑', '难过', '开心', '疲惫'].includes(keyword)) {
    return 'emotion';
  }
  return null;
}